package Learning

object Variables {
  def main(args: Array[String]) {
    val i = 4
    val d = 4.0
    val s = "HackerRank "

    val ni = scala.io.StdIn.readInt()
    val nd = scala.io.StdIn.readDouble()
    val ns = scala.io.StdIn.readLine()

    println(i + ni)
    println(d + nd)
    println(s + ns)

    //    var vs val
  }
}